package clispec

type AuthMode string

const (
	AuthModeNone            AuthMode = "none"
	AuthModeLocal           AuthMode = "local"
	AuthModeBearerOrSession AuthMode = "bearer_or_session"
	AuthModeSessionOnly     AuthMode = "session_only"
)

type OperationKind string

const (
	OperationLocal OperationKind = "local"
	OperationRead  OperationKind = "read"
	OperationWrite OperationKind = "write"
)

type IdempotencyMode string

const (
	IdempotencyNotApplicable IdempotencyMode = "not_applicable"
	IdempotencyLocalState    IdempotencyMode = "local_state"
	IdempotencyReadOnly      IdempotencyMode = "read_only"
	IdempotencyServerManaged IdempotencyMode = "server_managed"
)

type FlagSpec struct {
	Name     string `json:"name"`
	Required bool   `json:"required,omitempty"`
}

type PositionalSpec struct {
	Name     string `json:"name"`
	Required bool   `json:"required,omitempty"`
}

type CommandSpec struct {
	Section      string           `json:"section"`
	Path         []string         `json:"path"`
	Usage        string           `json:"usage"`
	Summary      string           `json:"summary"`
	SupportsJSON bool             `json:"supportsJson"`
	AuthMode     AuthMode         `json:"authMode"`
	AuthNote     string           `json:"authNote,omitempty"`
	Operation    OperationKind    `json:"operation"`
	Idempotency  IdempotencyMode  `json:"idempotency"`
	Positionals  []PositionalSpec `json:"positionals,omitempty"`
	Flags        []FlagSpec       `json:"flags,omitempty"`
	Notes        []string         `json:"notes,omitempty"`
}

type SectionSpec struct {
	Key   string   `json:"key"`
	Title string   `json:"title"`
	Notes []string `json:"notes,omitempty"`
}

type AuthModeSpec struct {
	Name        AuthMode `json:"name"`
	Label       string   `json:"label"`
	Description string   `json:"description"`
}

type Spec struct {
	SchemaVersion           string         `json:"schemaVersion"`
	PrimarySurface          string         `json:"primarySurface"`
	CanonicalMachineAPI     string         `json:"canonicalMachineApi"`
	CommandRegistrySource   string         `json:"commandRegistrySource"`
	ExtensionInstallSources []string       `json:"extensionInstallSources"`
	AuthModes               []AuthModeSpec `json:"authModes"`
	Sections                []SectionSpec  `json:"sections"`
	Commands                []CommandSpec  `json:"commands"`
}

