func Sections() []SectionSpec {
	return append([]SectionSpec(nil), sections...)
}

func Commands() []CommandSpec {
	return append([]CommandSpec(nil), commands...)
}

func CommandsForSection(section string) []CommandSpec {
	var filtered []CommandSpec
	for _, command := range commands {
		if command.Section == section {
			filtered = append(filtered, command)
		}
	}
	return filtered
}

func Section(section string) (SectionSpec, bool) {
	for _, candidate := range sections {
		if candidate.Key == section {
			return candidate, true
		}
	}
	return SectionSpec{}, false
}

func CurrentSpec() Spec {
	return Spec{
		SchemaVersion:           "mbr-cli-contract-v1",
		PrimarySurface:          "cli_first",
		CanonicalMachineAPI:     "graphql",
		CommandRegistrySource:   "internal/clispec",
		ExtensionInstallSources: append([]string(nil), extensionInstallSources...),
		AuthModes:               append([]AuthModeSpec(nil), authModes...),
		Sections:                Sections(),
		Commands:                Commands(),
	}
}

func AuthModeLabel(mode AuthMode) string {
	for _, candidate := range authModes {
		if candidate.Name == mode {
			return candidate.Label
		}
	}
	return string(mode)
}
